CREATE TABLE [dbo].[VENDA]
(
	[IDVENDA] INT NOT NULL  IDENTITY(1,1) PRIMARY KEY, 
    [IDCONTATO] INT NOT NULL, 
    [STATUS] INT NOT NULL, 
    [DATACRIACAO] DATETIME NOT NULL, 
    [DATAFINALIZACAO] DATETIME NOT NULL, 
    [DESCONTO] DECIMAL(3, 2) NOT NULL DEFAULT 0, 
    [VALOR] DECIMAL(18, 2) NOT NULL, 
    CONSTRAINT [FK_VENDA_CONTATO] FOREIGN KEY ([IDCONTATO]) REFERENCES [CONTATO]([IDCONTATO])
)
